<h3>Overview of this project.</h3>
<div id="improvement">
<div class="box">
<ul>
    <li><span id="improvement">概要・目的</span>
        <ul>
			<li>社内のあらゆる業務を対象として、</li>
			<li>コスト（人数、時間、お金 etc...）を縮小する事を第一の目的とし</li>
			<li>当該目的に対する手段を考察するプロジェクトです</li>
        </ul>
    </li>
    <li><span id="improvement">副次的な効果</span>
        <ul>
			<li>目的をひとつずつ積み上げる事で、コストの圧縮とは別に</li>
			<li>担当者が「煩雑で手間がかかり集中力を要する業務」から少しでも解放され</li>
			<li>本来業務に集中する事によって「生産性の向上」「精神的負荷の低減」が期待できます</li>
        </ul>
    </li>
    <li><span id="improvement">手段の考え方 一例</span>
        <ul>
			<li>作業量・時間を減らす＞人員削減</li>
			<li>ミス防止を目的とした確認作業やミス発生時のフォローアップの手間を減らす</li>
			<li>資料やドキュメントファイルを共有＞バージョン管理負荷や印刷コストを減らす etc...</li>
        </ul>
    </li>
</ul>
</div>
</div>
