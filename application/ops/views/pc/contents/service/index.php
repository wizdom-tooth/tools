<h2 class="style_h2_01">VWP（ビザ免除プログラム）とは？</h2>
<h3 class="style_h3_03">■ビザ免除プログラム（Visa Waiver Program：VWP）</h3>
<p class="style_p14">「ビザ免除プログラム（VWP）」は、特定の国籍の方が米国に渡航する場合、有効なパスポート、往復または次の目的地までの航空券・乗船券を所持し渡米目的が短期の商用や観光であればビザなしで90日以下の滞在が可能となるプログラムです。<br />
VWP対象国の方はビザを申請することもできます。全ての国がビザ免除プログラムに参加しているわけではありません。
また、VWP参加国のすべての方が、ビザ免除プログラムを利用できるというわけでもありません。
渡米に際して、ビザ免除渡航者は、電子渡航認証システム（ESTA）で認証され、米国入国地で認証を受ける必要があり、
米国国土安全保障省（DHS）のUS-VISITプログラムに登録の必要があります。</p>
<div class="space_20"></div>

<h3 class="style_h3_03">■パスポート要件について</h3>
<p class="style_p14">ビザ免除渡航者は、有効な機械読取式パスポート(写真IDページの下に2行で記号表示があるもの)を所持する必要があります。なお、パスポートの発行日により、下記の条件を満たさなければなりません。</p>

<ul class="esta_ul">
<li>2006年10月26日以降に発行されている機械読取式パスポートは、e-パスポート（IC旅券）でなければなりません。</li>
<li>2005年10月26日から2006年10月25日までに発行されている機械読取式パスポートは、写真付きIDページにデジタル写真が貼ってあること。</li>
<li>2005年10月26日より前に発行されているパスポートは機械読取式パスポートであること。</li>
<li>新規VWP参入国（ギリシャ、マルタ、チェコ、エストニア、ハンガリー、ラトビア、リトアニア、韓国、スロバキア）の
国籍の方は、パスポートの発効日に関わらず、渡米の際はe-パスポート（IC旅券）を所持すること。</li>
</ul>
<div class="space_20"></div>

<h3 class="style_h3_03">■ビザ免除プログラムを利用できない場合</h3>

<p class="style_p14">有罪判決の有無にかかわらず逮捕歴のある方、犯罪歴（恩赦や大赦などの法的措置がとられた場合も含む）がある方、
重い伝染病を患っている方、過去に米国への入国を拒否されたり強制送還された方、そしてビザ免除プログラムで入国し、
オーバーステイしたことがある方は、ビザ免除プログラムを利用することはできません。
渡米するためには、ビザを取得しなければなりません。
ビザを持たずに入国しようとする場合は入国を拒否されることがあります。</p>

<p class="style_p14">※逮捕や有罪に至らないような交通違反の場合は、その他のビザ免除プログラムの条件を満たしていれば、このプログラムの利用が可能です。米国滞在中に交通違反を犯し罰金未払いあるいは法廷審問に出頭しなかったような場合は、逮捕状が出されている可能性もあり、入国審査で問題になることが予想されます。
渡米前に管轄の裁判所に連絡を取り、問題をご確認下さい。</p>
<div class="space_20"></div>

<h3 class="style_h3_03">■ビザ免除プログラム（VWP）は、現在下記の国籍の方に適用されています</h3>
<p class="style_p18">VWP参加国：日本/アンドラ/オーストラリア/オーストリア/ベルギー/ブルネイ/チェコ/デンマーク/エストニア/フィンランド/フランス/ドイツ/ギリシャ/ハンガリー/アイスランド/アイルランド/イタリア/ラトビア/リヒテンシュタイン/リトアニア/ルクセンブルグ/モナコ/オランダ/ニュージーランド/ノルウェイ/ポルトガル/マルタ/サンマリノ/シンガポール/スロバキア/スロベニア/韓国/スペイン/スウェーデン/スイス/英国/台湾</p>

<ul class="esta_ul">
<li>VWPパスポート条件に当たる有効な旅券、又は電子旅券をお持ちの方である事</li>
<li>電子渡航認証システム（ESTA）により渡航認証が承認されている事</li>
<li>滞在する期間が90日以下で、目的が観光・商用である事（経由含む）</li>
<li>その他のVWPの条件を満たす事</li>
</ul>

<p class="style_p14">以上の条件を満たしアメリカ合衆国50州（ハワイ・アラスカへの渡航者を含む）もしくはアメリカ経由で他国に渡航する方</p>

<p class="style_p17">※渡米目的が長期滞在、就労、留学等の方（例：米国への渡航目的が長期留学であり、90日以上の滞在を予定している方）はビザ免除プログラムを
利用できません。この場合、ESTA申請も不要となる替わりに、ビザの取得が必要となります。</p>

