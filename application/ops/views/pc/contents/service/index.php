<h2 class="style_h2_01">ESTA(電子渡航認証システム)とは？</h2>
<h3 class="style_h3_03">■『ESTA（電子渡航認証システム）』とビザは違います</h3>
<p class="style_p14">「ESTA（エスタ）」とは、正式名称を電子渡航認証システム（ Electorical System for Travel Authorization ）
と言い、米国国土安全保障省（DHS）により2009年1月12日からビザを取得せずに入国する全ての外国人に対して
義務化されました。</p>
<p class="style_p14">このシステムはビザ免除プログラムの一部で、アメリカへ短期商用・観光目的で滞在日数が90日以内の場合アメリカ大使館でビザ取得は免除されていますが航空機や船に搭乗する前にオンラインシステムを利用し渡航認証を受けなければなりません。
この渡航認証システムが「ESTA（エスタ）」です。（事前にビザを取得し渡航する方はESTA申請は不要です。）</p>
<p class="style_p14">「ESTA（エスタ）」は、旅行の許容のための事前委任です。
そのため、米国政府の国土安全保障省は、入国する72時間以内の渡航者は「ESTA（エスタ）」を通じて旅行許可の申請を勧めています。</p>

<p class="style_p14">以下の条件に当てはまる場合、申請が可能です。</p>
<ul class="esta_ul">
<li>VWP（ビザ免除プログラム）に参加している国の国籍を持っている</li>
<li>短期商用か観光が目的であること</li>
<li>有効なパスポートを持っていること</li>
<li>往復、または次の目的地の航空チケット、船舶チケットを持っていること</li>
<li>90日以内の滞在である事</li>
</ul>

<p class="style_p15">ESTA申請を拒否された場合は、米国大使館または米国領事館を通じてビザを申請しなければなりません。
ビザ発行には時間がかかるのでご注意下さい。
米国での乗り継ぎをする場合も事前に認証を受ける必要があります。</p>

<p class="style_p14">ESTAの申請が完了しますと渡航認証許可、保留、却下のいずれかのステータスが与えられます。許可された場合の有効期限は2年間、又はパスポートの有効満了日のいずれか短い方となります。
もし万が一、ESTAの申請が拒否された場合には米国大使館にてビザを取得しなければ米国への渡航はできませんのでご注意下さい。
その他、詳しい情報については、米国国土安全保障省サイト（英語）や、米国大使館サイト（日本語）をご参照下さい。</p>

<p class="style_p14">尚、アメリカのビザに関する質問を米国大使館・領事館へ行う場合には費用が掛かります。</p>

<p class="style_p16">米国大使館：<a href="http://japanese.japan.usembassy.gov/" target="_blank" class="a_color">http://japanese.japan.usembassy.gov/</a></p>
