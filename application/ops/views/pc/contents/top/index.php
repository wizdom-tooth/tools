            <h2>Electronic System for Travel Authorization</h2>
            <p class="about_desc">ESTA(エスタ)とは、正式名称を電子渡航認証システム(Electronic System for Travel Authorization:エスタ)と言い、米国国土安全保障省により2009年1月12日からビザを取得せずに入国する全ての外国人に対してESTA(エスタ)の事前申請が義務化されました。</p>
            <p class="about_desc">アメリカ入国の目的が短期商用・観光等で、滞在希望日数が90日以内の場合(乗り継ぎを含む)、米国大使館でのビザ取得は免除されますが、渡航前にオンラインシステムを利用して渡航認証を受ける必要があり、この渡航認証システムがESTA(エスタ)です。<br />
            (事前にビザを取得して渡航する方はESTAの申請は不要です。)</p>
            <p class="about_desc">ESTA(エスタ)は、旅行の許容のための事前委任です。<br />
            そのため、米国政府の国土安全保障省は、入国する72時間以内の渡航者は、ESTA(エスタ)を通じて旅行許可の申請を勧めています。</p>
            <div class="space_30"></div>
